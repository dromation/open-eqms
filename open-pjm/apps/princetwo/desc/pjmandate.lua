The Project Mandate typically includes the following information:

Project title and description: This section provides a brief description of the project and its objectives.

Background: This section provides the context for the project, outlining the business issue or problem the project aims to address.

Business case: This section outlines the reasons why the project is needed and provides a summary of the benefits, costs, and risks associated with the project.

Initial scope: This section outlines the initial scope of the project, including any constraints or assumptions.

High-level requirements: This section outlines the high-level requirements for the project.

Budget: This section outlines the estimated budget for the project.

Timeline: This section outlines the estimated timeline for the project.

Project approach: This section outlines the proposed approach for the project, including any methodology or tools to be used.

Project organization: This section outlines the roles and responsibilities of the project team and stakeholders.

Sign-off: This section provides a place for the sponsor or other authorized person to sign off on the Project Mandate and authorize the project to proceed.